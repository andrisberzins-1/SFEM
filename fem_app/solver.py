def validate_model(model: ModelDefinition) -> Optional[str]:
    """
    Validate a ModelDefinition for completeness and consistency.

    Returns None if valid, or a human-readable error string.
    """
    if not model.nodes:
        return "Model has no nodes defined."
    if not model.members:
        return "Model has no members defined."
    if not model.materials:
        return "Model has no materials defined."
    if not model.cross_sections:
        return "Model has no cross-sections defined."
    if not model.supports:
        return "Model has no supports defined."
    if not model.loads:
        return "Model has no loads defined."

    # Check for duplicate node IDs
    node_ids = [n.id for n in model.nodes]
    if len(node_ids) != len(set(node_ids)):
        dupes = [nid for nid in node_ids if node_ids.count(nid) > 1]
        return f"Duplicate node IDs found: {set(dupes)}"

    # Check for duplicate member IDs
    member_ids = [m.id for m in model.members]
    if len(member_ids) != len(set(member_ids)):
        dupes = [mid for mid in member_ids if member_ids.count(mid) > 1]
        return f"Duplicate member IDs found: {set(dupes)}"

    # Check for duplicate material IDs
    mat_ids = [mat.id for mat in model.materials]
    if len(mat_ids) != len(set(mat_ids)):
        dupes = [mid for mid in mat_ids if mat_ids.count(mid) > 1]
        return f"Duplicate material IDs found: {set(dupes)}"

    # Check for duplicate cross-section IDs
    sec_ids = [s.id for s in model.cross_sections]
    if len(sec_ids) != len(set(sec_ids)):
        dupes = [sid for sid in sec_ids if sec_ids.count(sid) > 1]
        return f"Duplicate cross-section IDs found: {set(dupes)}"

    # Validate material properties
    for mat in model.materials:
        if mat.E_GPa <= 0:
            return f"Material {mat.id} has invalid E_GPa={mat.E_GPa}. Must be positive."

    # Validate cross-section references and properties
    mat_id_set = set(mat_ids)
    for sec in model.cross_sections:
        if sec.material_id not in mat_id_set:
            return f"Cross-section {sec.id} references non-existent material {sec.material_id}."
        if sec.A_cm2 <= 0:
            return f"Cross-section {sec.id} has invalid A_cm2={sec.A_cm2}. Must be positive."
        if sec.Iz_cm4 <= 0:
            return f"Cross-section {sec.id} has invalid Iz_cm4={sec.Iz_cm4}. Must be positive."

    node_id_set = set(node_ids)
    sec_id_set = set(sec_ids)
    member_id_set = set(member_ids)

    # Check members reference valid nodes, sections, and are not zero-length
    for m in model.members:
        if m.start_node not in node_id_set:
            return f"Member {m.id} references non-existent start node {m.start_node}."
        if m.end_node not in node_id_set:
            return f"Member {m.id} references non-existent end node {m.end_node}."
        if m.start_node == m.end_node:
            return f"Member {m.id} has the same start and end node ({m.start_node}). Members must connect two different nodes."
        if m.section_id not in sec_id_set:
            return f"Member {m.id} references non-existent cross-section {m.section_id}."

    # Check duplicate node coordinates
    coords = [(n.x, n.y) for n in model.nodes]
    if len(coords) != len(set(coords)):
        for i, c1 in enumerate(coords):
            for j, c2 in enumerate(coords):
                if i < j and c1 == c2:
                    return (
                        f"Nodes {model.nodes[i].id} and {model.nodes[j].id} "
                        f"have the same coordinates ({c1[0]}, {c1[1]})."
                    )

    # Check supports reference valid nodes
    for s in model.supports:
        if s.node_id not in node_id_set:
            return f"Support references non-existent node {s.node_id}."
        if s.type not in VALID_SUPPORT_TYPES:
            return f"Support at node {s.node_id} has invalid type '{s.type}'."
        if s.type.startswith("spring") and s.spring_stiffness is None:
            return f"Spring support at node {s.node_id} requires a spring_stiffness value."

    # Check loads reference valid nodes/members
    for load in model.loads:
        if load.type not in VALID_LOAD_TYPES:
            return f"Load {load.id} has invalid type '{load.type}'."
        if load.type == "UDL":
            if load.node_or_member_id not in member_id_set:
                return f"UDL load {load.id} references non-existent member {load.node_or_member_id}."
            if load.direction not in ("Fx", "Fy"):
                return f"UDL load {load.id} has invalid direction '{load.direction}'. UDL supports Fx or Fy only."
        else:
            if load.node_or_member_id not in node_id_set:
                return f"Load {load.id} references non-existent node {load.node_or_member_id}."

    # Check hinges reference valid members
    hinged_members = set()
    for h in model.hinges:
        if h.member_id not in member_id_set:
            return f"Hinge references non-existent member {h.member_id}."
        if not h.start_release and not h.end_release:
            return f"Hinge on member {h.member_id} has no releases. At least one end must be released."
        if h.member_id in hinged_members:
            return f"Duplicate hinge definition for member {h.member_id}."
        hinged_members.add(h.member_id)

    # Check that supported nodes are connected to at least one member
    connected_nodes = set()
    for m in model.members:
        connected_nodes.add(m.start_node)
        connected_nodes.add(m.end_node)

    for s in model.supports:
        if s.node_id not in connected_nodes:
            return (
                f"Support at node {s.node_id} is not connected to any member. "
                f"All supported nodes must be part of the structure."
            )

    return None


