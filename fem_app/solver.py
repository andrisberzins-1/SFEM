def get_diagram_data(model: ModelDefinition) -> Optional[dict[str, Any]]:
    """
    Solve the model and return detailed arrays for plotting diagrams.

    Returns None if model is invalid. Otherwise returns dict with keys:
        "members": list of dicts per member, each containing:
            - member_id, x_coords, y_coords (undeformed)
            - dx, dy (displacement arrays)
            - M, Q, N arrays (bending moment, shear, axial)
        "nodes": list of dicts per node with id, x, y, dx_mm, dy_mm
    """
    error = validate_model(model)
    if error:
        return None

    try:
        ss, node_id_map, elem_id_map = _build_system(model)
        ss.solve(verbosity=1)
    except Exception:
        return None

    rev_elem_map = {v: k for k, v in elem_id_map.items()}

    diagram_data: dict[str, Any] = {"members": [], "nodes": []}

    # Build hinge lookup for diagram data
    hinge_map = {h.member_id: h for h in model.hinges}

    for an_eid, model_mid in rev_elem_map.items():
        el = ss.element_map[an_eid]
        el_res = ss.get_element_results(element_id=an_eid, verbose=True)

        n_points = len(el_res.get("N", [])) if el_res.get("N") is not None else 50

        # Undeformed coordinates along the element
        # Use el.vertex_1/vertex_2 (not el.node_1.vertex) for correct positions
        x1, y1 = el.vertex_1.x, el.vertex_1.y
        x2, y2 = el.vertex_2.x, el.vertex_2.y
        x_coords = np.linspace(x1, x2, n_points).tolist()
        y_coords = np.linspace(y1, y2, n_points).tolist()

        # Displacement arrays
        defl = el_res.get("wtot")
        ext = el_res.get("u")

        # Check if member has hinges at both ends (truss-like behavior)
        h = hinge_map.get(model_mid)
        is_truss = h is not None and h.start_release and h.end_release

        member_data = {
            "member_id": model_mid,
            "x_coords": x_coords,
            "y_coords": y_coords,
            "N": el_res.get("N", np.zeros(n_points)).tolist(),
            "Q": el_res.get("Q", np.zeros(n_points)).tolist(),
            "M": el_res.get("M", np.zeros(n_points)).tolist(),
            "deflection": defl.tolist() if defl is not None else [0.0] * n_points,
            "extension": ext.tolist() if ext is not None else [0.0] * n_points,
            "length": el.l,
            "angle": el.angle,
            "is_truss": is_truss,
        }
        diagram_data["members"].append(member_data)

    # Build node coordinate lookup from model definition
    node_coords = {n.id: (n.x, n.y) for n in model.nodes}

    for model_nid, an_nid in node_id_map.items():
        disp = ss.get_node_displacements(node_id=an_nid)
        nx, ny = node_coords[model_nid]
        diagram_data["nodes"].append({
            "node_id": model_nid,
            "x": nx,
            "y": ny,
            "dx_mm": disp["ux"] * M_TO_MM,
            "dy_mm": disp["uy"] * M_TO_MM,
        })

    return diagram_data


