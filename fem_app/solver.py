"""
solver.py — Single source of truth for all FEM analysis.

This module wraps the anastruct library and provides a clean interface
for building, solving, and extracting results from 2D structural models.

Neither app.py nor api.py may import anastruct directly — all FEM
interactions go through this module.

Units throughout:
    Forces:       kN
    Lengths:      m
    Moments:      kNm
    Distributed:  kN/m
    Modulus E:    GPa  (converted to kN/m² internally: 1 GPa = 1e6 kN/m²)
    Area A:       cm²  (converted to m² internally: 1 cm² = 1e-4 m²)
    Inertia Iz:   cm⁴  (converted to m⁴ internally: 1 cm⁴ = 1e-8 m⁴)
    Displacement: mm   (in results only)
"""

from __future__ import annotations

import copy
import math
from dataclasses import dataclass, field
from datetime import datetime
from typing import Any, Optional

import numpy as np
import yaml
from anastruct import SystemElements
from anastruct.basic import FEMException

# ---------------------------------------------------------------------------
# Constants
# ---------------------------------------------------------------------------

# Conversion factors
GPA_TO_KN_M2 = 1e6        # 1 GPa = 1 000 000 kN/m²
CM2_TO_M2 = 1e-4           # 1 cm² = 0.0001 m²
CM4_TO_M4 = 1e-8           # 1 cm⁴ = 0.00000001 m⁴
M_TO_MM = 1000.0           # 1 m = 1000 mm

# Sanity check threshold — displacements beyond this indicate numerical instability
MAX_DISPLACEMENT_M = 1e6   # 1000 km — clearly nonsensical

# Valid enumerations
VALID_SUPPORT_TYPES = {
    "fixed", "pinned", "roller_x", "roller_y", "rotational",
    "spring_linear_x", "spring_linear_y", "spring_rotational",
}
# NOTE: "free" was removed — a node with no support entry is simply free.
VALID_LOAD_TYPES = {"point_force", "UDL", "point_moment", "settlement"}
VALID_DIRECTIONS = {"Fx", "Fy", "Mz"}
VALID_STRUCTURE_TYPES = {"beam", "truss", "frame", "custom"}

# YAML file header
YAML_HEADER = """\
# -----------------------------------------------
# 2D FEM Model File
# Generated by FEM App v1.0
# Units: kN, m, kNm, GPa, cm2, cm4
# Edit this file directly or load it in the app
# -----------------------------------------------

"""

# ---------------------------------------------------------------------------
# Data classes
# ---------------------------------------------------------------------------


@dataclass
class NodeDef:
    """A node in the structural model."""
    id: int
    x: float  # m
    y: float  # m


@dataclass
class MemberDef:
    """A member (element) connecting two nodes."""
    id: int
    start_node: int
    end_node: int
    section_id: int = 1             # references CrossSectionDef.id


@dataclass
class SupportDef:
    """A support condition at a node."""
    node_id: int
    type: str = "pinned"            # see VALID_SUPPORT_TYPES
    spring_stiffness: Optional[float] = None  # kN/m or kNm/rad


@dataclass
class LoadDef:
    """A load applied to a node or member."""
    id: int
    type: str                       # see VALID_LOAD_TYPES
    node_or_member_id: int
    direction: str = "Fy"           # "Fx", "Fy", or "Mz"
    magnitude: float = 0.0          # kN, kN/m, or kNm depending on type
    udl_start: Optional[float] = None  # m from start node (None = full span)
    udl_end: Optional[float] = None    # m from start node (None = full span)


@dataclass
class MaterialDef:
    """A material definition."""
    id: int
    name: str = "Steel S275"
    E_GPa: float = 210.0  # GPa


@dataclass
class CrossSectionDef:
    """A cross-section definition referencing a material."""
    id: int
    name: str = "Custom"
    A_cm2: float = 100.0    # cm²
    Iz_cm4: float = 10000.0  # cm⁴
    material_id: int = 1     # references MaterialDef.id


@dataclass
class HingeDef:
    """Moment release (hinge) at one or both ends of a member."""
    member_id: int
    start_release: bool = False  # True = free rotation at start node
    end_release: bool = False    # True = free rotation at end node


@dataclass
class ModelDefinition:
    """Complete structural model definition."""
    structure_type: str = "frame"
    name: str = ""
    description: str = ""
    nodes: list[NodeDef] = field(default_factory=list)
    members: list[MemberDef] = field(default_factory=list)
    materials: list[MaterialDef] = field(default_factory=list)
    cross_sections: list[CrossSectionDef] = field(default_factory=list)
    supports: list[SupportDef] = field(default_factory=list)
    loads: list[LoadDef] = field(default_factory=list)
    hinges: list[HingeDef] = field(default_factory=list)
    mesh_size: int = 50  # FE sub-elements per member for mesh discretisation


@dataclass
class ReactionResult:
    """Reaction forces at a support node."""
    node_id: int
    Rx_kN: float
    Ry_kN: float
    Mz_kNm: float


@dataclass
class MemberResult:
    """Summary results for a single member."""
    member_id: int
    N_max_kN: float
    V_max_kN: float
    M_max_kNm: float
    max_displacement_mm: float
    M_max_location_m: float
    V_max_location_m: float
    N_signed_kN: float = 0.0  # signed axial: positive=tension, negative=compression


@dataclass
class NodeDisplacement:
    """Displacement of a node after solving."""
    node_id: int
    dx_mm: float
    dy_mm: float
    rz_mrad: float  # milliradians


@dataclass
class SolveResult:
    """Complete solution output."""
    status: str = "ok"
    error: Optional[str] = None
    reactions: list[ReactionResult] = field(default_factory=list)
    member_results: list[MemberResult] = field(default_factory=list)
    nodes_displaced: list[NodeDisplacement] = field(default_factory=list)
    warnings: list[str] = field(default_factory=list)


