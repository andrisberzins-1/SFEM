def _build_system(model: ModelDefinition) -> tuple[SystemElements, dict[int, int], dict[int, int]]:
    """
    Build an anastruct SystemElements from a ModelDefinition.

    Returns:
        ss: The SystemElements object (unsolved).
        node_id_map: Maps model node ID -> anastruct node ID.
        elem_id_map: Maps model member ID -> anastruct element ID.
    """
    # anastruct assigns its own sequential IDs, so we track the mapping
    ss = SystemElements(EA=1e6, EI=1e4, mesh=model.mesh_size)

    node_id_map: dict[int, int] = {}   # model node ID -> anastruct node ID
    elem_id_map: dict[int, int] = {}   # model member ID -> anastruct element ID

    # Build lookup maps
    node_coords = {n.id: (n.x, n.y) for n in model.nodes}
    mat_map = {m.id: m for m in model.materials}
    sec_map = {s.id: s for s in model.cross_sections}
    hinge_map = {h.member_id: h for h in model.hinges}

    # Add elements (this also creates nodes in anastruct)
    for member in model.members:
        x1, y1 = node_coords[member.start_node]
        x2, y2 = node_coords[member.end_node]

        # Resolve section -> material -> properties
        sec = sec_map[member.section_id]
        mat = mat_map[sec.material_id]
        EA = mat.E_GPa * GPA_TO_KN_M2 * sec.A_cm2 * CM2_TO_M2  # kN
        EI = mat.E_GPa * GPA_TO_KN_M2 * sec.Iz_cm4 * CM4_TO_M4  # kNm²

        # Build spring parameter for hinges (moment releases)
        spring = None
        if member.id in hinge_map:
            h = hinge_map[member.id]
            spring = {}
            if h.start_release:
                spring[1] = 0
            if h.end_release:
                spring[2] = 0

        elem_id = ss.add_element(
            location=[[x1, y1], [x2, y2]],
            EA=EA,
            EI=EI,
            spring=spring if spring else None,
        )

        elem_id_map[member.id] = elem_id

    # Map model node IDs to anastruct node IDs by coordinate lookup.
    # We cannot rely on element node_id1/node_id2 ordering because anastruct
    # may internally swap start/end nodes relative to the input coordinates.
    for model_node in model.nodes:
        an_nid = ss.find_node_id(vertex=[model_node.x, model_node.y])
        node_id_map[model_node.id] = an_nid

    # Add supports
    for support in model.supports:
        an_node = node_id_map[support.node_id]

        if support.type == "fixed":
            ss.add_support_fixed(node_id=an_node)
        elif support.type == "pinned":
            ss.add_support_hinged(node_id=an_node)
        elif support.type == "roller_x":
            # Free to slide in X, restrained in Y
            ss.add_support_roll(node_id=an_node, direction="x")
        elif support.type == "roller_y":
            # Free to slide in Y, restrained in X
            ss.add_support_roll(node_id=an_node, direction="y")
        elif support.type == "rotational":
            # Restrained rotation only, free to translate
            ss.add_support_rotational(node_id=an_node)
        elif support.type == "spring_linear_x":
            ss.add_support_spring(
                node_id=an_node, translation=1, k=support.spring_stiffness
            )
        elif support.type == "spring_linear_y":
            ss.add_support_spring(
                node_id=an_node, translation=2, k=support.spring_stiffness
            )
        elif support.type == "spring_rotational":
            ss.add_support_spring(
                node_id=an_node, translation=3, k=support.spring_stiffness
            )

    # Pre-process UDL loads: group by member to handle multiple UDLs on same member.
    # anastruct's q_load() overwrites previous calls on the same element, so we must
    # combine Fx and Fy UDLs into a single q_load call using the q_perp parameter.
    from collections import defaultdict
    udl_groups: dict[int, dict[str, float]] = defaultdict(lambda: {"Fx": 0.0, "Fy": 0.0})
    for load in model.loads:
        if load.type == "UDL":
            udl_groups[load.node_or_member_id][load.direction] += load.magnitude

    # Add loads
    applied_udl_members: set[int] = set()
    for load in model.loads:
        if load.type == "point_force":
            an_node = node_id_map[load.node_or_member_id]
            fx = load.magnitude if load.direction == "Fx" else 0.0
            fy = load.magnitude if load.direction == "Fy" else 0.0
            # anastruct with invert_y_loads=True: positive Fy = downward
            # Our convention: negative magnitude = downward force
            # So we negate Fy to match anastruct's inverted convention
            ss.point_load(node_id=an_node, Fx=fx, Fy=-fy)

        elif load.type == "UDL":
            member_id = load.node_or_member_id
            if member_id in applied_udl_members:
                continue  # Already applied combined UDL for this member
            applied_udl_members.add(member_id)

            an_elem = elem_id_map[member_id]
            total_qx = udl_groups[member_id]["Fx"]  # positive = rightward
            total_qy = udl_groups[member_id]["Fy"]   # our convention: negative = downward

            # Convert to anastruct convention:
            # direction="y" with invert_y_loads: positive q = downward
            # q_perp with direction="y": perpendicular to y = x-direction
            qy_an = -total_qy  # negate because anastruct inverts Y
            qx_an = total_qx   # x-direction, no inversion

            if total_qx != 0.0 and total_qy != 0.0:
                # Both directions present — use q_perp to apply in single call
                ss.q_load(q=qy_an, element_id=an_elem, direction="y", q_perp=qx_an)
            elif total_qy != 0.0:
                # Fy only
                ss.q_load(q=qy_an, element_id=an_elem, direction="y")
            elif total_qx != 0.0:
                # Fx only
                ss.q_load(q=qx_an, element_id=an_elem, direction="x")

        elif load.type == "point_moment":
            an_node = node_id_map[load.node_or_member_id]
            ss.moment_load(node_id=an_node, Tz=load.magnitude)

    return ss, node_id_map, elem_id_map


def solve(model: ModelDefinition) -> SolveResult:
    """
    Validate, build, and solve a structural model.

    Args:
        model: A complete ModelDefinition with nodes, members, supports, loads.

    Returns:
        SolveResult with status "ok" and populated results, or
        status "error" with a human-readable error message.
    """
    # Validate first
    error = validate_model(model)
    if error:
        return SolveResult(status="error", error=error)

    try:
        ss, node_id_map, elem_id_map = _build_system(model)
    except FEMException as e:
        return SolveResult(status="error", error=f"Model build error: {e.message}")
    except Exception as e:
        return SolveResult(status="error", error=f"Failed to build model: {str(e)}")

    # Solve
    try:
        ss.solve(verbosity=1)
    except FEMException as e:
        return SolveResult(status="error", error=f"Analysis failed: {e.message}")
    except AssertionError as e:
        msg = str(e)
        if "forces" in msg.lower():
            return SolveResult(
                status="error",
                error="No loads are applied to the structure.",
            )
        return SolveResult(
            status="error",
            error=f"Analysis failed: the structure may be unstable or ill-conditioned. ({msg})",
        )
    except np.linalg.LinAlgError:
        return SolveResult(
            status="error",
            error=(
                "The stiffness matrix is singular — the structure is a mechanism "
                "or has insufficient supports. Check that all degrees of freedom "
                "are properly restrained."
            ),
        )
    except Exception as e:
        return SolveResult(status="error", error=f"Unexpected solver error: {str(e)}")

    # Post-solve sanity check: detect numerically unstable solutions
    try:
        for an_nid in node_id_map.values():
            disp = ss.get_node_displacements(node_id=an_nid)
            if abs(disp["ux"]) > MAX_DISPLACEMENT_M or abs(disp["uy"]) > MAX_DISPLACEMENT_M:
                return SolveResult(
                    status="error",
                    error=(
                        "The structure is unstable — displacements are unreasonably large. "
                        "This usually means the structure is a mechanism (e.g. collinear truss "
                        "members, or insufficient supports for the applied loads)."
                    ),
                )
    except Exception:
        pass  # If displacement check fails, proceed with result extraction

    # Extract results
    result = SolveResult(status="ok")

    # Reverse maps for looking up model IDs from anastruct IDs
    rev_node_map = {v: k for k, v in node_id_map.items()}
    rev_elem_map = {v: k for k, v in elem_id_map.items()}

    # --- Reactions ---
    for an_node_id, node in ss.reaction_forces.items():
        model_node_id = rev_node_map.get(an_node_id, an_node_id)
        result.reactions.append(ReactionResult(
            node_id=model_node_id,
            Rx_kN=round(node.Fx, 6),
            Ry_kN=round(node.Fy, 6),
            Mz_kNm=round(node.Tz, 6),
        ))

    # Sort reactions by node ID
    result.reactions.sort(key=lambda r: r.node_id)

    # Build hinge lookup for result extraction
    hinge_map = {h.member_id: h for h in model.hinges}

    # --- Member results ---
    for model_mid, an_eid in elem_id_map.items():
        el_res = ss.get_element_results(element_id=an_eid, verbose=True)
        el = ss.element_map[an_eid]

        # Determine max absolute values
        n_min = el_res.get("Nmin", 0)
        n_max_val = el_res.get("Nmax", 0)
        n_max = max(abs(n_min), abs(n_max_val))
        n_signed = n_min if abs(n_min) >= abs(n_max_val) else n_max_val
        v_max = max(abs(el_res.get("Qmin", 0)), abs(el_res.get("Qmax", 0)))
        m_max = max(abs(el_res.get("Mmin", 0)), abs(el_res.get("Mmax", 0)))

        # Max displacement along the element
        defl_array = el_res.get("wtot")
        if defl_array is not None and len(defl_array) > 0:
            max_disp_m = float(np.max(np.abs(defl_array)))
        else:
            max_disp_m = max(abs(el_res.get("wmin", 0)), abs(el_res.get("wmax", 0)))

        # Location of max moment and shear (distance from start node)
        length = el.l
        m_loc = 0.0
        v_loc = 0.0

        m_array = el_res.get("M")
        if m_array is not None and len(m_array) > 0:
            m_idx = int(np.argmax(np.abs(m_array)))
            m_loc = round(m_idx / (len(m_array) - 1) * length, 4) if len(m_array) > 1 else 0.0

        q_array = el_res.get("Q")
        if q_array is not None and len(q_array) > 0:
            v_idx = int(np.argmax(np.abs(q_array)))
            v_loc = round(v_idx / (len(q_array) - 1) * length, 4) if len(q_array) > 1 else 0.0

        result.member_results.append(MemberResult(
            member_id=model_mid,
            N_max_kN=round(n_max, 6),
            V_max_kN=round(v_max, 6),
            M_max_kNm=round(m_max, 6),
            max_displacement_mm=round(max_disp_m * M_TO_MM, 6),
            M_max_location_m=round(m_loc, 4),
            V_max_location_m=round(v_loc, 4),
            N_signed_kN=round(n_signed, 6),
        ))

    result.member_results.sort(key=lambda r: r.member_id)

    # --- Node displacements ---
    for model_nid, an_nid in node_id_map.items():
        disp = ss.get_node_displacements(node_id=an_nid)
        result.nodes_displaced.append(NodeDisplacement(
            node_id=model_nid,
            dx_mm=round(disp["ux"] * M_TO_MM, 6),
            dy_mm=round(disp["uy"] * M_TO_MM, 6),
            rz_mrad=round(disp["phi_z"] * 1000, 6),  # rad -> mrad
        ))

    result.nodes_displaced.sort(key=lambda r: r.node_id)

    # --- Equilibrium check ---
    # Sum applied loads (point forces only; UDL resultants need member lengths)
    total_fx = 0.0
    total_fy = 0.0
    nc = {n.id: (n.x, n.y) for n in model.nodes}
    for load in model.loads:
        if load.type == "point_force":
            if load.direction == "Fx":
                total_fx += load.magnitude
            elif load.direction == "Fy":
                total_fy += load.magnitude
        elif load.type == "UDL":
            mid = load.node_or_member_id
            member = next((m for m in model.members if m.id == mid), None)
            if member:
                x1, y1 = nc[member.start_node]
                x2, y2 = nc[member.end_node]
                length = ((x2 - x1) ** 2 + (y2 - y1) ** 2) ** 0.5
                if load.direction == "Fy":
                    total_fy += load.magnitude * length
                elif load.direction == "Fx":
                    total_fx += load.magnitude * length

    sum_rx = sum(r.Rx_kN for r in result.reactions)
    sum_ry = sum(r.Ry_kN for r in result.reactions)
    residual_x = abs(sum_rx + total_fx)
    residual_y = abs(sum_ry + total_fy)
    max_load = max(abs(total_fx), abs(total_fy), 1.0)
    tol = 1e-2 * max_load

    if residual_x > tol or residual_y > tol:
        result.warnings.append(
            f"Equilibrium check failed: force residuals "
            f"(Fx={residual_x:.3f} kN, Fy={residual_y:.3f} kN) "
            f"exceed tolerance {tol:.3f} kN. Results may be unreliable."
        )

    return result


